--STATISTICI DAUNE
CREATE OR REPLACE VIEW STATISTICI_DAUNE AS
SELECT
    M.NUME_MARCA,
    MOD.NUME_MODEL,
    V.VIN,
    COUNT(D.ID_DAUNA) AS TOTAL_INCIDENTE,
    NVL(SUM(D.COST_ESTIMAT), 0) AS COST_TOTAL_REPARATII
FROM VEHICUL V
JOIN MODEL MOD ON V.ID_MODEL = MOD.ID_MODEL
JOIN MARCA M ON MOD.ID_MARCA = M.ID_MARCA
LEFT JOIN DAUNE D ON V.VIN = D.VIN
GROUP BY M.NUME_MARCA, MOD.NUME_MODEL, V.VIN;

SELECT * FROM STATISTICI_DAUNE;

