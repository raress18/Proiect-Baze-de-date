-- 1. Tabelul MARCA

CREATE TABLE MARCA (
    ID_MARCA NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NUME_MARCA VARCHAR2(50) NOT NULL,
    TARA_PROVENIENTA VARCHAR2(50)
);

-- 2. Tabelul MODEL
CREATE TABLE MODEL (
    ID_MODEL NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_MARCA NUMBER(10) NOT NULL,
    NUME_MODEL VARCHAR2(50) NOT NULL,
    CONSTRAINT FK_MODEL_MARCA FOREIGN KEY (ID_MARCA) REFERENCES MARCA (ID_MARCA)
);

-- 3. Tabelul MECANIC
CREATE TABLE MECANIC (
    CNP_MECANIC CHAR(13) PRIMARY KEY,
    NUME VARCHAR2(50) NOT NULL,
    PRENUME VARCHAR2(50) NOT NULL,
    TELEFON VARCHAR2(15),
    EMAIL VARCHAR2(100) NOT NULL UNIQUE,
    SALARIU NUMBER(10, 2),
    CONSTRAINT CHK_SALARIU_MECANIC CHECK (SALARIU > 0)
);

-- 4. Tabelul VEHICUL
CREATE TABLE VEHICUL (
    VIN VARCHAR2(17) PRIMARY KEY,
    ID_MODEL NUMBER(10) NOT NULL,
    CNP_MECANIC CHAR(13) NOT NULL,
    AN_FABRICATIE NUMBER(4),
    KILOMETRAJ NUMBER(10),
    TIP_COMBUSTIBIL VARCHAR2(20),
    CAPACITATE_CILINDRICA NUMBER(5),
    TRACTIUNE VARCHAR2(20),
    TIP_CUTIE VARCHAR2(20),
    STATUS_VEHICUL VARCHAR2(20),
    CONSTRAINT FK_VEHICUL_MODEL FOREIGN KEY (ID_MODEL) REFERENCES MODEL(ID_MODEL),
    CONSTRAINT FK_VEHICUL_MECANIC FOREIGN KEY (CNP_MECANIC) REFERENCES MECANIC(CNP_MECANIC),
    CONSTRAINT CHK_AN_FABRICATIE CHECK (AN_FABRICATIE > 1900),
    CONSTRAINT CHK_KILOMETRAJ CHECK (KILOMETRAJ >= 0),
    CONSTRAINT CHK_CAPACITATE CHECK (CAPACITATE_CILINDRICA > 0)
);

-- 5. Tabelul DAUNE
CREATE TABLE DAUNE (
    ID_DAUNA NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VIN VARCHAR2(17) NOT NULL,
    DESCRIERE VARCHAR2(500),
    COST_ESTIMAT NUMBER(10, 2),
    SEVERITATE VARCHAR2(20),
    CONSTRAINT FK_DAUNA_VEHICUL FOREIGN KEY (VIN) REFERENCES VEHICUL(VIN) ON DELETE CASCADE,
    CONSTRAINT CHK_COST_DAUNA CHECK (COST_ESTIMAT >= 0)
);

-- 6. Tabelul PARTICIPANT
CREATE TABLE PARTICIPANT (
    CNP_PARTICIPANT CHAR(13) PRIMARY KEY,
    NUME VARCHAR2(50) NOT NULL,
    PRENUME VARCHAR2(50) NOT NULL,
    TELEFON VARCHAR2(15),
    EMAIL VARCHAR2(100) NOT NULL UNIQUE,
    SOLD_CURENT NUMBER(10, 2) DEFAULT 0,
    DATA_INSCRIERE DATE DEFAULT SYSDATE,
    CONSTRAINT CHK_SOLD_PARTICIPANT CHECK (SOLD_CURENT >= 0)
);

-- 7. Tabelul LICITATIE
CREATE TABLE LICITATIE (
    ID_LICITATIE NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VIN VARCHAR2(17) NOT NULL,
    DATA_INCEPUT DATE NOT NULL,
    DATA_SFARSIT DATE NOT NULL,
    PRET_PORNIRE NUMBER(10, 2) NOT NULL,
    STATUS_LICITATIE VARCHAR2(20),
    CONSTRAINT FK_LICITATIE_VEHICUL FOREIGN KEY (VIN) REFERENCES VEHICUL(VIN),
    CONSTRAINT CHK_DATE_LICITATIE CHECK (DATA_SFARSIT > DATA_INCEPUT),
    CONSTRAINT CHK_PRET_PORNIRE CHECK (PRET_PORNIRE > 0),
    CONSTRAINT CHK_STATUS_LICITATIE CHECK (STATUS_LICITATIE IN ('Activa', 'Incheiata', 'Anulata'))
);

-- 8. Tabelul OFERTA
CREATE TABLE OFERTA (
    ID_OFERTA NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_LICITATIE NUMBER(10) NOT NULL,
    CNP_PARTICIPANT CHAR(13) NOT NULL,
    SUMA_OFERITA NUMBER(10, 2) NOT NULL,
    DATA_OFERTA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_OFERTA_LICITATIE FOREIGN KEY (ID_LICITATIE) REFERENCES LICITATIE(ID_LICITATIE),
    CONSTRAINT FK_OFERTA_PARTICIPANT FOREIGN KEY (CNP_PARTICIPANT) REFERENCES PARTICIPANT(CNP_PARTICIPANT),
    CONSTRAINT CHK_SUMA_OFERITA CHECK (SUMA_OFERITA > 0)
);

