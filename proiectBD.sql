SELECT NMA.NUME_MARCA,
       NMO.NUME_MODEL,
       V.VIN,
       V.AN_FABRICATIE,
       V.KILOMETRAJ,
       V.STATUS_VEHICUL
FROM MARCA NMA
JOIN MODEL NMO ON NMO.ID_MARCA = NMA.ID_MARCA
JOIN VEHICUL V ON NMO.ID_MODEL = V.ID_MODEL
WHERE V.STATUS_VEHICUL = 'Licitatie'
AND V.KILOMETRAJ < 150000;

--AFISARE TOP N CEI MAI ACTIVI PARTICIPANTI (IN FUNCTIE DE NUMARUL DE OFERTE)
SELECT * FROM (SELECT P.NUME,
                      P.PRENUME,
                      COUNT(O.ID_OFERTA)  AS NUMAR_OFERTE,
                      MAX(O.SUMA_OFERITA) AS OFERTA_MAXIMA
               FROM PARTICIPANT P
                        JOIN OFERTA O ON P.CNP_PARTICIPANT = O.CNP_PARTICIPANT
               GROUP BY P.NUME, P.PRENUME
               HAVING COUNT(O.ID_OFERTA) >= 1
               ORDER BY NUMAR_OFERTE DESC
) WHERE ROWNUM <= :n;

